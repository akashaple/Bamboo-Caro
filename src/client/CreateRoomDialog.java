    public void processResultRequestNewRoom(boolean bool) {
        if (bool) {
            this.dispose();
            this.dashBoardPanel.openCanvasPanel(givenRoom.getMap(), givenRoom.getMap().getNodeSize(), givenRoom.getHostType(), true);
            this.dashBoardPanel.setTitle(givenRoom.getName());
            this.dashBoardPanel.setIsHost(true);
        } else {
            JOptionPane.showMessageDialog(this, "Room name is already existed!",
                    "Error!", JOptionPane.ERROR_MESSAGE);
        }
    }
