private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    String roomName = this.txtRoomName.getText().trim();
    if (roomName.length() == 0) {
        JOptionPane.showMessageDialog(this, "Please enter room name!",
                "Error!", JOptionPane.ERROR_MESSAGE);
        return;
    }
    GameMap map = new GameMap(xSize, ySize);
    int player1Element = this.rdoFire.isSelected() ? Element.FIRE : Element.ICE;
    map.setPlayer1Element(player1Element);
    map.setNodeSize(nodeSize);
    givenRoom = new Room(roomName, nodeSize);
    givenRoom.setMap(map);
    givenRoom.setHostType(player1Element);
    Client client = this.dashBoardPanel.getClient();
    try {
        client.sendInt(Cmd.NEW_ROOM);
        client.sendObject(givenRoom);
        isTurn = true;
    } catch (IOException ex) {
        Logger.getLogger(CreateRoomDialog.class.getName()).log(Level.SEVERE, null, ex);
    }


}//GEN-LAST:event_jButton1ActionPerformed

